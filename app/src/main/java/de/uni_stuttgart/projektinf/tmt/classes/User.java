package de.uni_stuttgart.projektinf.tmt.classes;

/**
 * The User Class.
 * This class represents a digital user. It contains all the necessary data of the user and handles it.
 */
public class User {

    private int gender;
    private int age;
    private int sight;
    private int rightHanded;


    public User (int gender, int age, int sight, int rightHanded){
        this.gender = gender;
        this.age = age;
        this.sight = sight;
        this.rightHanded = rightHanded;

    }


}
